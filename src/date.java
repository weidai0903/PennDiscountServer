
public class date {
	private String year;
	private String month;
	private String day;
	public void date(String y, String m, String d){
		this.year=y;
		this.month=m;
		this.day=d;
	}
	public String getYear() {
		return year;
	}
	public void setYear(String year) {
		this.year = year;
	}
	public String getMonth() {
		return month;
	}
	public void setMonth(String month) {
		this.month = month;
	}
	public String getDay() {
		return day;
	}
	public void setDay(String day) {
		this.day = day;
	}
	
}
